package com.example.whiteball.utilities;

public class Position {

    private double x;
    private double y;

    public Position(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
